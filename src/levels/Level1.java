    public Level1(MyGame myGame) {
        super(myGame);
        this.map = LoadSave.getMap(pathOfMap(MY_LEVEL));
    }

    // Draw the level, import image of the level
    @Override
    public void draw(Graphics g) {
        super.draw(g);
        this.map = LoadSave.getMap(pathOfMap(MY_LEVEL));
        g.drawImage(map, 0, 0, MyGame.WIDTH, MyGame.HEIGHT, null);
        check(getTiles(), g);
    }

    // Set the barrier or the wall of the level
    public void changeTiles(int x, int y) {
        this.tiles[y][x] = 1;
    }

    public int[][] getTiles() {
        return tiles;
    }

    public void check(int[][] mapData, Graphics g) {
        for (int i = 0; i < mapData.length; i++) {
            for (int j = 0; j < mapData[i].length; j++) {
                if (mapData[i][j] == 1) {
                    g.setColor(Color.RED);
                    g.drawRect(j * 32, i * 32, 32, 32);
                }
            }
        }
    }
